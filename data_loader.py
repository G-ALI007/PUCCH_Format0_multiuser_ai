def reconstruct_complex(X: np.ndarray) -> np.ndarray:
    """
    Reconstruct complex samples from real-valued feature matrix.

    The feature matrix format is:
        [real_1, real_2, ..., real_12, imag_1, imag_2, ..., imag_12]

    This function combines them into complex numbers:
        complex_i = real_i + j * imag_i

    Parameters:
    -----------
    X : np.ndarray
        Feature matrix of shape (num_samples, 24)
        Columns 0-11: real parts
        Columns 12-23: imaginary parts

    Returns:
    --------
    X_complex : np.ndarray
        Complex array of shape (num_samples, 12)
        dtype: complex64

    Raises:
    -------
    ValueError
        If X does not have exactly 24 columns

    Example:
    --------
    >>> X = np.random.randn(100, 24).astype(np.float32)
    >>> X_complex = reconstruct_complex(X)
    >>> print(X_complex.shape, X_complex.dtype)
    (100, 12) complex64
    """

    # Validate input
    if X.ndim != 2:
        raise ValueError(f"Expected 2D array, got {X.ndim}D")

    if X.shape[1] != 24:
        raise ValueError(f"Expected 24 columns, got {X.shape[1]}")

    # Extract real and imaginary parts
    real_parts = X[:, :12]   # Columns 0-11
    imag_parts = X[:, 12:]   # Columns 12-23

    # Combine into complex numbers
    X_complex = (real_parts + 1j * imag_parts).astype(np.complex64)

    return X_complex


def split_complex_to_real(X_complex: np.ndarray) -> np.ndarray:
    """
    Split complex samples into real-valued feature matrix.

    This is the inverse of reconstruct_complex().

    Parameters:
    -----------
    X_complex : np.ndarray
        Complex array of shape (num_samples, 12)

    Returns:
    --------
    X : np.ndarray
        Feature matrix of shape (num_samples, 24)
        Columns 0-11: real parts
        Columns 12-23: imaginary parts
        dtype: float32

    Raises:
    -------
    ValueError
        If X_complex does not have exactly 12 columns

    Example:
    --------
    >>> X_complex = np.random.randn(100, 12) + 1j * np.random.randn(100, 12)
    >>> X = split_complex_to_real(X_complex.astype(np.complex64))
    >>> print(X.shape, X.dtype)
    (100, 24) float32
    """

    # Validate input
    if X_complex.ndim != 2:
        raise ValueError(f"Expected 2D array, got {X_complex.ndim}D")

    if X_complex.shape[1] != 12:
        raise ValueError(f"Expected 12 columns, got {X_complex.shape[1]}")

    # Extract real and imaginary parts
    real_parts = np.real(X_complex)
    imag_parts = np.imag(X_complex)

    # Concatenate
    X = np.concatenate([real_parts, imag_parts], axis=1).astype(np.float32)

    return X


