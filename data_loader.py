def load_dataset(
    snr_db: int,
    data_dir: Optional[str] = None,
    validate: bool = True,
    verbose: bool = True
) -> Tuple[np.ndarray, np.ndarray]:
    """
    Load dataset for a single SNR value from CSV file.

    Parameters:
    -----------
    snr_db : int
        SNR value in dB (e.g., 0, 5, 10, 15, 20)
    data_dir : str, optional
        Directory containing the CSV files
        If None, uses config.DATA_DIR
    validate : bool, default=True
        If True, validate file structure before loading
    verbose : bool, default=True
        If True, print progress information

    Returns:
    --------
    X : np.ndarray
        Feature matrix of shape (num_samples, 24)
        dtype: float32
        Columns 0-11: real parts (real_1 to real_12)
        Columns 12-23: imaginary parts (imag_1 to imag_12)
    y : np.ndarray
        Label vector of shape (num_samples,)
        dtype: int32
        Values: 0, 1, 2, or 3

    Raises:
    -------
    FileNotFoundError
        If the CSV file does not exist
    ValueError
        If file validation fails

    Example:
    --------
    >>> X, y = load_dataset(snr_db=10)
    Loading pucch_f0_dataset_SNR_10dB.csv... Done! Shape: (200000, 24), Time: 2.5s
    >>> print(X.shape, y.shape)
    (200000, 24) (200000,)
    """

    # Set default data directory
    if data_dir is None:
        data_dir = config.DATA_DIR

    # Construct filename and filepath
    filename = f"pucch_f0_dataset_SNR_{snr_db}dB.csv"
    filepath = os.path.join(data_dir, filename)

    # Validate file if requested
    if validate:
        is_valid, message, details = validate_csv_file(
            filepath, check_all_rows=False, verbose=False)
        if not is_valid:
            raise ValueError(f"Invalid file '{filename}': {message}")

    # Load data
    if verbose:
        print(f"Loading {filename}...", end=" ", flush=True)

    start_time = time.time()

    # Read CSV file
    try:
        df = pd.read_csv(filepath)
    except Exception as e:
        raise RuntimeError(f"Failed to read '{filename}': {str(e)}")

    # Extract features (columns 0-23)
    X = df[config.FEATURE_COLUMNS].values.astype(np.float32)

    # Extract labels (column 24)
    y = df[config.LABEL_COLUMN].values.astype(np.int32)

    elapsed_time = time.time() - start_time

    if verbose:
        print(f"Done! Shape: {X.shape}, Time: {elapsed_time:.2f}s")

    return X, y


def load_all_datasets(
    snr_values: Optional[List[int]] = None,
    data_dir: Optional[str] = None,
    validate: bool = True,
    verbose: bool = True
) -> Dict[int, Tuple[np.ndarray, np.ndarray]]:
    """
    Load datasets for all specified SNR values.

    Parameters:
    -----------
    snr_values : list of int, optional
        List of SNR values to load
        If None, uses config.SNR_VALUES
    data_dir : str, optional
        Directory containing CSV files
        If None, uses config.DATA_DIR
    validate : bool, default=True
        If True, validate each file before loading
    verbose : bool, default=True
        If True, print progress information

    Returns:
    --------
    datasets : dict
        Dictionary mapping SNR values to (X, y) tuples
        Key: int (SNR in dB)
        Value: tuple of (X, y) where
            X: np.ndarray of shape (num_samples, 24)
            y: np.ndarray of shape (num_samples,)

    Raises:
    -------
    RuntimeError
        If no datasets could be loaded

    Example:
    --------
    >>> datasets = load_all_datasets()
    ======================================================================
    LOADING DATASETS
    ======================================================================
    Loading pucch_f0_dataset_SNR_0dB.csv... Done! Shape: (200000, 24), Time: 2.3s
    Loading pucch_f0_dataset_SNR_5dB.csv... Done! Shape: (200000, 24), Time: 2.4s
    ...
    ----------------------------------------------------------------------
    Successfully loaded: 5/5 datasets
    ======================================================================

    >>> print(datasets.keys())
    dict_keys([0, 5, 10, 15, 20])
    """

    # Set defaults
    if snr_values is None:
        snr_values = config.SNR_VALUES
    if data_dir is None:
        data_dir = config.DATA_DIR

    if verbose:
        print("\n" + "=" * 70)
        print("LOADING DATASETS")
        print("=" * 70)

    datasets = {}
    errors = []

    for snr in snr_values:
        try:
            X, y = load_dataset(
                snr_db=snr,
                data_dir=data_dir,
                validate=validate,
                verbose=verbose
            )
            datasets[snr] = (X, y)

        except FileNotFoundError as e:
            error_msg = f"File not found"
            if verbose:
                print(f"  WARNING: SNR {snr} dB - {error_msg}")
            errors.append((snr, error_msg))

        except ValueError as e:
            error_msg = str(e)
            if verbose:
                print(f"  ERROR: SNR {snr} dB - {error_msg}")
            errors.append((snr, error_msg))

        except Exception as e:
            error_msg = str(e)
            if verbose:
                print(f"  ERROR: SNR {snr} dB - {error_msg}")
            errors.append((snr, error_msg))

    if verbose:
        print("-" * 70)
        print(
            f"Successfully loaded: {len(datasets)}/{len(snr_values)} datasets")

        if errors:
            print(f"Failed to load: {len(errors)} datasets")
            for snr, msg in errors:
                print(f"  - SNR {snr} dB: {msg}")

        print("=" * 70 + "\n")

    # Check if any datasets were loaded
    if len(datasets) == 0:
        raise RuntimeError(
            f"No datasets could be loaded from '{data_dir}'. "
            f"Please check that CSV files exist and are valid."
        )

    return datasets


