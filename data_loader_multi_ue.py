def analyze_multi_ue_dataset(
    X: np.ndarray,
    y: np.ndarray,
    scenario: str,
    name: str = "Multi-User Dataset",
    verbose: bool = True
) -> Dict:
    """
    Perform comprehensive analysis of a Multi-User dataset.

    Parameters:
    -----------
    X : np.ndarray
        Feature matrix of shape (num_samples, num_features)
    y : np.ndarray
        Label vector of shape (num_samples,)
    scenario : str
        Scenario name ("2users" or "3users")
    name : str, default="Multi-User Dataset"
        Name for identification in output
    verbose : bool, default=True
        If True, print analysis results

    Returns:
    --------
    stats : dict
        Dictionary containing all statistics
    """

    stats = {}

    # ----- Basic information -----
    stats['name'] = name
    stats['scenario'] = scenario
    stats['num_samples'] = int(X.shape[0])
    stats['num_features'] = int(X.shape[1])
    stats['feature_dtype'] = str(X.dtype)
    stats['label_dtype'] = str(y.dtype)

    # ----- Feature statistics -----
    stats['feature_min'] = float(np.min(X))
    stats['feature_max'] = float(np.max(X))
    stats['feature_mean'] = float(np.mean(X))
    stats['feature_std'] = float(np.std(X))
    stats['feature_median'] = float(np.median(X))

    # ----- Data quality -----
    stats['num_nan'] = int(np.sum(np.isnan(X)))
    stats['num_inf'] = int(np.sum(np.isinf(X)))
    stats['num_zero_rows'] = int(np.sum(np.all(X == 0, axis=1)))

    # ----- Class distribution -----
    unique_labels, counts = np.unique(y, return_counts=True)

    stats['class_distribution'] = {
        int(label): int(count)
        for label, count in zip(unique_labels, counts)
    }

    stats['class_percentages'] = {
        int(label): float(count / len(y) * 100)
        for label, count in zip(unique_labels, counts)
    }

    # Class balance
    if len(counts) > 0 and min(counts) > 0:
        stats['class_balance_ratio'] = float(max(counts) / min(counts))
    else:
        stats['class_balance_ratio'] = float('inf')

    stats['is_balanced'] = stats['class_balance_ratio'] < 1.1

    # ----- Print results -----
    if verbose:
        print(f"\n{'='*60}")
        print(f"DATASET ANALYSIS: {name}")
        print(f"Scenario: {scenario}")
        print(f"{'='*60}")

        print(f"\n--- Basic Information ---")
        print(f"Number of samples: {stats['num_samples']:,}")
        print(f"Number of features: {stats['num_features']}")
        print(f"Feature dtype: {stats['feature_dtype']}")
        print(f"Label dtype: {stats['label_dtype']}")

        print(f"\n--- Feature Statistics ---")
        print(f"Min: {stats['feature_min']:.6f}")
        print(f"Max: {stats['feature_max']:.6f}")
        print(f"Mean: {stats['feature_mean']:.6f}")
        print(f"Std: {stats['feature_std']:.6f}")
        print(f"Median: {stats['feature_median']:.6f}")

        print(f"\n--- Data Quality ---")
        print(f"NaN values: {stats['num_nan']}")
        print(f"Inf values: {stats['num_inf']}")
        print(f"All-zero rows: {stats['num_zero_rows']}")

        print(f"\n--- Class Distribution ---")
        for label in sorted(stats['class_distribution'].keys()):
            count = stats['class_distribution'][label]
            pct = stats['class_percentages'][label]
            class_name = config_multi_ue.CLASS_LABELS.get(
                label, f"Class {label}")
            print(
                f"  Class {label}: {count:,} samples ({pct:.2f}%) - {class_name}")

        print(f"\nClass balance ratio: {stats['class_balance_ratio']:.4f}")
        balance_status = "YES" if stats['is_balanced'] else "NO"
        print(f"Is balanced (within 10%): {balance_status}")

        print(f"{'='*60}\n")

    return stats


