def validate_multi_ue_csv(
    filepath: str,
    check_all_rows: bool = False,
    verbose: bool = False
) -> Tuple[bool, str, Dict]:
    """
    Validate that a Multi-User CSV file has the correct structure and content.

    This function checks:
        1. File exists and is not empty
        2. Correct number of columns (25)
        3. Correct column names (real_1...real_12, imag_1...imag_12, label)
        4. No NaN or Inf values
        5. Label values are valid (0, 1, 2, 3)
        6. Data types are correct

    Parameters:
    -----------
    filepath : str
        Full path to the CSV file
    check_all_rows : bool, default=False
        If True, check entire file (slower but thorough)
    verbose : bool, default=False
        If True, print validation details

    Returns:
    --------
    is_valid : bool
        True if file passes all validation checks
    message : str
        Description of validation result or error
    details : dict
        Dictionary containing validation details
    """

    details = {
        'num_rows': 0,
        'num_columns': 0,
        'file_size_mb': 0.0,
        'unique_labels': set(),
        'has_nan': False,
        'has_inf': False
    }

    # ----- Check 1: File exists -----
    if not os.path.exists(filepath):
        return False, f"File not found: {filepath}", details

    # ----- Check 2: File is not empty -----
    file_size = os.path.getsize(filepath)
    details['file_size_mb'] = file_size / (1024 * 1024)

    if file_size == 0:
        return False, "File is empty (0 bytes)", details

    if verbose:
        print(f"  File size: {details['file_size_mb']:.2f} MB")

    # ----- Check 3: Read and validate structure -----
    try:
        if check_all_rows:
            df = pd.read_csv(filepath)
        else:
            # Read first 1000 rows for quick validation
            df = pd.read_csv(filepath, nrows=1000)

        details['num_rows'] = len(df)
        details['num_columns'] = len(df.columns)

        if verbose:
            print(f"  Rows checked: {details['num_rows']}")
            print(f"  Columns: {details['num_columns']}")

    except pd.errors.EmptyDataError:
        return False, "File has no data (only header or empty)", details
    except pd.errors.ParserError as e:
        return False, f"CSV parsing error: {str(e)}", details
    except Exception as e:
        return False, f"Error reading file: {str(e)}", details

    # ----- Check 4: Number of columns -----
    expected_columns = config_multi_ue.EXPECTED_NUM_COLUMNS
    actual_columns = len(df.columns)

    if actual_columns != expected_columns:
        return False, f"Expected {expected_columns} columns, found {actual_columns}", details

    # ----- Check 5: Column names -----
    expected_column_names = config_multi_ue.FEATURE_COLUMNS + \
        [config_multi_ue.LABEL_COLUMN]
    actual_column_names = list(df.columns)

    if actual_column_names != expected_column_names:
        mismatches = []
        for i, (exp, act) in enumerate(zip(expected_column_names, actual_column_names)):
            if exp != act:
                mismatches.append(f"Column {i}: expected '{exp}', got '{act}'")

        if mismatches:
            return False, f"Column name mismatch: {mismatches[0]}", details
        else:
            return False, "Column order or names do not match expected format", details

    # ----- Check 6: NaN values -----
    nan_count = df.isnull().sum().sum()
    details['has_nan'] = nan_count > 0

    if nan_count > 0:
        nan_columns = df.columns[df.isnull().any()].tolist()
        return False, f"Found {nan_count} NaN values in columns: {nan_columns}", details

    # ----- Check 7: Feature data type (must be numeric) -----
    try:
        features = df[config_multi_ue.FEATURE_COLUMNS].values
        features_float = features.astype(np.float32)

        # Check for Inf values
        inf_count = np.isinf(features_float).sum()
        details['has_inf'] = inf_count > 0

        if inf_count > 0:
            return False, f"Found {inf_count} Inf values in features", details

    except (ValueError, TypeError) as e:
        return False, f"Feature columns contain non-numeric data: {str(e)}", details

    # ----- Check 8: Label values -----
    try:
        labels = df[config_multi_ue.LABEL_COLUMN].values
        labels_int = labels.astype(np.int32)

        unique_labels = set(labels_int)
        details['unique_labels'] = unique_labels

        valid_labels = set(range(config_multi_ue.NUM_CLASSES))
        invalid_labels = unique_labels - valid_labels

        if invalid_labels:
            return False, f"Invalid label values found: {invalid_labels}. Expected: {valid_labels}", details

        if verbose:
            print(f"  Unique labels: {sorted(unique_labels)}")

    except (ValueError, TypeError) as e:
        return False, f"Label column contains non-integer data: {str(e)}", details

    # ----- All checks passed -----
    return True, "File is valid", details


